#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	dstlen;
	size_t	srclen;
	size_t	c;
	size_t	d;

	while (*dst++)
		dstlen++;
	while (*src++)
		srclen++;
	if (size <= dstlen)
		return (size + srclen);
	c = dstlen;
	d = 0;
	while (src[d] != '\0' && c + 1 < size)
	{
		dst[c] = src[d];
		c++;
		d++;
	}
	dst[c] = '\0';
	return (dstlen + ft_strlen(&src[d]));
}
