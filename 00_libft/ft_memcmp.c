#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	if (n == 0)
		return (0);
	i = 0;
	while (i <= n)
	{
		if ((*s1 == *s2) && (*s1 != '\0') && (*s2 != '\0'))
			i++;
		else
			return ((unsigned char *)(s1 - s2));
	}
	return (0);
}
